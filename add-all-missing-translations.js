console.log('🔄 Adding all missing translations to all languages...\n');

let totalAdded = 0;
languages.forEach(lang => {
  const added = updateLanguageFile(lang);
  totalAdded += added;
});

console.log(`\n🎉 Successfully added ${totalAdded} total translations across all languages!`);
