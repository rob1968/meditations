const languages = ['en', 'nl', 'de', 'fr', 'es', 'it', 'ja', 'ko', 'pt', 'ru', 'zh', 'ar', 'hi'];

// Function to load and update a language file
function updateLanguageFile(lang) {
  const filePath = path.join(__dirname, 'frontend', 'src', 'locales', lang, 'translation.json');
  
  let existingData = {};
  try {
    if (fs.existsSync(filePath)) {
      const fileContent = fs.readFileSync(filePath, 'utf8');
      existingData = JSON.parse(fileContent);
    }
  } catch (error) {
    console.error(`Error reading ${lang}/translation.json:`, error.message);
    existingData = {};
  }

  let addedCount = 0;
  
  // Add missing translations
  Object.keys(allTranslations).forEach(key => {
    if (!existingData[key] && allTranslations[key][lang]) {
      existingData[key] = allTranslations[key][lang];
      addedCount++;
    }
  });

  // Sort keys alphabetically
  const sortedData = {};
  Object.keys(existingData).sort().forEach(key => {
    sortedData[key] = existingData[key];
  });

  try {
    // Write updated file
    fs.writeFileSync(filePath, JSON.stringify(sortedData, null, 2) + '\n', 'utf8');
    console.log(`✅ ${lang.toUpperCase()}: Added ${addedCount} missing translations`);
  } catch (error) {
    console.error(`❌ ${lang.toUpperCase()}: Failed to write file:`, error.message);
  }

  return addedCount;
}

